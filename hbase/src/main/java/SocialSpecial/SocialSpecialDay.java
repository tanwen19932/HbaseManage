package SocialSpecial;
public class SocialSpecialDay {
	private String specialIdDay;
	private String specialId;
	private long time;
	private String textAll;
	private String keyword;
	private String locSentiment;
	private String locSentimentOrient;
	private String locHitCnt;
	private String day;
	public String getSpecialId() {
		return specialId;
	}
	public void setSpecialId(String specialId) {
		this.specialId = specialId;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public String getSpecialIdDay() {
		return specialIdDay;
	}
	public void setSpecialIdDay(String specialIdDay) {
		this.specialIdDay = specialIdDay;
	}
	public String getTextAll() {
		return textAll;
	}
	public void setTextAll(String textAll) {
		this.textAll = textAll;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public String getLocSentiment() {
		return locSentiment;
	}
	public void setLocSentiment(String locSentiment) {
		this.locSentiment = locSentiment;
	}
	public String getLocSentimentOrient() {
		return locSentimentOrient;
	}
	public void setLocSentimentOrient(String locSentimentOrient) {
		this.locSentimentOrient = locSentimentOrient;
	}
	public String getLocHitCnt() {
		return locHitCnt;
	}
	public void setLocHitCnt(String locHitCnt) {
		this.locHitCnt = locHitCnt;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
}
